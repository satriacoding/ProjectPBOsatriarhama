public class Manusia {
    private int tinggi, berat;
    private String kulit, nama;  
    
    public int get_tinggi(){
        return tinggi;
    }
    
    public void set_tinggi(int tinggi){
        this.tinggi = tinggi;
    }
    
    public int get_berat(){
        return berat;
    }
    
    public void set_berat(int berat){
        this.berat = berat;
    }
    
    public String get_kulit(){
        return kulit;
    }
    
    public void set_kulit(String kulit){
        this.kulit = kulit;
    }
    
    public String get_nama(){
        return nama;
    }
    
    public void set_nama(String nama){
        this.nama = nama;
    }
}
